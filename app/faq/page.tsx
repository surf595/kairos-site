function FaqSection({
  title,
  items,
}: {
  title: string
  items: { question: string; answer: string }[]
}) {
  return (
    <section className="mb-14">
      <h2 className="mb-6 text-3xl font-semibold tracking-tight text-neutral-900">
        {title}
      </h2>

      <div className="space-y-4">
        {items.map((item) => (
          <FaqItem
            key={item.question}
            question={item.question}
            answer={item.answer}
          />
        ))}
      </div>
    </section>
  )
}

